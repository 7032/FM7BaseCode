/* ============================================================
 * c_sound.h — PSG (AY-3-8910) サウンドの C API
 *
 * FM-7 のサウンドは AY-3-8910 (PSG): 矩形波 3ch + ノイズ 1 系統。
 * メイン CPU 側 I/O ($FD0D=BDIR/BC1 制御, $FD0E=データバス) で叩くので、
 * グラフィックのサブシステム (HALT/共有 RAM/subprog) とは完全に独立。
 *
 * チャンネル割り当て (= 競合させない単純構成):
 *   ch A : SE  ボール発射音 (ノイズ・バースト + 減衰)
 *   ch B : SE  歩行音 (短い低音ブリップ)
 *   ch C : BGM 単音メロディ (単調なループ)
 * mixer (R7) は起動時に固定し、 各音の ON/OFF は音量レジスタで行う。
 *
 * 駆動はフレーム同期: メインループ先頭のフレームペーシング (= timer による
 * 約 32ms 単位の deadline (= FRAME_TARGET=16 × 約2ms)) のあと sound_tick() を 1 回呼ぶ。 BGM の音長カウントと
 * SE の減衰/リリースはすべてこのティックで進む。
 *
 * 仕組みの詳細は docs/SOUND.md。
 * ============================================================ */
#ifndef C_SOUND_H
#define C_SOUND_H

/* 起動時 1 回。 PSG を初期化 (mixer 設定 + 全 ch 消音) し、 BGM を開始する。 */
void sound_init(void);

/* 毎フレーム 1 回 (= フレームペーシングの直後)。 BGM を 1 ステップ進め、
 * 鳴っている SE があれば減衰/リリースする。 */
void sound_tick(void);

/* ボール発射音をトリガ (ch A: ノイズ・バースト)。 BREAK 発射の瞬間に呼ぶ。 */
void sfx_ball(void);

/* 歩行音をトリガ (ch B: 短い低音ブリップ)。 歩行アニメが 1 歩進む瞬間に呼ぶ。 */
void sfx_walk(void);

#endif
